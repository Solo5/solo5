#define ENTRY(x) .text; .globl x; .type x,%function; x:
#define END(x)   .size x, . - x

.text
.code64

ENTRY(cpu_halt)
    cli
    hlt
    jmp cpu_halt
END(cpu_halt)

ENTRY(cpu_rdtsc)
    rdtsc
    shl $32, %rdx
    add %rdx, %rax
    ret
END(cpu_rdtsc)
